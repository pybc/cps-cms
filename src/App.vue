<template>
  <div id="app" :class="[loginStatus ? `bg-slate-50` : ``]">
    <SideBar v-if="loginStatus" />
    <TopBar v-if="loginStatus" />
    <router-view :class="[loginStatus ? `xl:ml-64` : ``]" />
  </div>
</template>

<script>
import { mapGetters } from "vuex";
import SideBar from "./components/SideBar.vue";
import TopBar from "./components/TopBar.vue";
export default {
  components: {
    SideBar,
    TopBar,
  },
  data() {
    return {
      profile: [],
    };
  },
  computed: {
    ...mapGetters({
      userProfile: "auth/getUserProfile",
    }),
    loginStatus() {
      return this.profile.length ? true : false;
    },
  },
  created() {
    this.profile = [...this.userProfile];
  },
};
</script>

<style>
#app {
  font-family: Inter, Helvetica, Arial, sans-serif;
  height: 100vh;
  overflow: auto;
}
